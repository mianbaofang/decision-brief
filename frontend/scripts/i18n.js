  const LOCALE_MAP = {
    'zh-CN': 'zh-CN',
    'yue': 'zh-HK',
    'en': 'en-US',
    'fr': 'fr-FR',
    'ja': 'ja-JP',
    'es': 'es-ES'
  };

  let current = 'zh-CN';

  function t(key, vars) {
    const dict = DICT[current] || DICT['zh-CN'];
    let s = dict[key];
    if (s == null) s = DICT['zh-CN'][key];
    if (s == null) return key;
    if (vars) {
      for (const k in vars) {
        s = s.split('{' + k + '}').join(vars[k]);
      }
    }
    return s;
  }

  function setLang(lang) {
    if (DICT[lang]) current = lang;
  }
  function getLang() { return current; }
  function getLocale() { return LOCALE_MAP[current] || 'zh-CN'; }
  function listLangs() { return Object.keys(DICT); }

  /** 应用翻译到 DOM：[data-i18n] 文本 / [data-i18n-ph] placeholder / [data-i18n-aria] aria-label / [data-i18n-title] title */
  function apply(root = document) {
    root.querySelectorAll('[data-i18n]').forEach(n => {
      n.textContent = t(n.getAttribute('data-i18n'));
    });
    root.querySelectorAll('[data-i18n-ph]').forEach(n => {
      n.setAttribute('placeholder', t(n.getAttribute('data-i18n-ph')));
    });
    root.querySelectorAll('[data-i18n-aria]').forEach(n => {
      n.setAttribute('aria-label', t(n.getAttribute('data-i18n-aria')));
    });
    root.querySelectorAll('[data-i18n-title]').forEach(n => {
      n.setAttribute('title', t(n.getAttribute('data-i18n-title')));
    });
    document.documentElement.setAttribute('lang', getLocale());
  }

  return { t, setLang, getLang, getLocale, listLangs, apply, DICT };
